/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM16_Init();
  MX_TIM3_Init();
  MX_ADC_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */

  /* tim16 pwm 1MHz D=50% start */
  HAL_TIM_PWM_Start(&htim16, TIM_CHANNEL_1);

  /* tim3 pwm 1kHz buzzer start */
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

  /* tim3 pwm 1kHz water pump start */
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_4);

  /* calib adc; adc dma start */
  uint16_t ADC_Raw[1];

  HAL_ADCEx_Calibration_Start(&hadc);
  HAL_ADC_Start_DMA(&hadc, (uint32_t*)ADC_Raw, 1);

  /* tim1 10Hz int+adc start */
  HAL_TIM_Base_Start_IT(&htim1);

  /* soil vars */
  uint8_t Soil_Swt = 255;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  if(Sch_100ms){

		  /* voltage from sensor */
		  Soil_Moi_V = 3.3*(ADC_Raw[0]/(float)4095.0);

		  if(ADC_Raw[0] >= SOIL_DRY){

			  /* water pump */
			  TIM3->CCR4 = 1000-1; // pump turn on

			  /* buzzer */
			  if(Soil_Swt){
				  TIM3->CCR2 = 800-1; // buzzer on
			  }
			  else
			  {
				  TIM3->CCR2 = 0; // buzzer off
			  }
			  Soil_Swt = ~Soil_Swt;
		  }

		  else if(ADC_Raw[0] <= SOIL_WET){
			  /* water pump */
			  TIM3->CCR4 = 0; // pump turn off

			  /* buzzer */
			  TIM3->CCR2 = 0; // buzzer off
		  }


		 /*nullify*/
		  Sch_100ms = 0;
	  }



    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

